print("Ejemplo estaciones condicionales")
print("Introduzca una estacion (1-4)")
estacion = int(input())
if (estacion == 1):
    print("VERANO")
elif (estacion == 2):
    print("INVIERNO")
elif (estacion == 3):
    print("PRIMAVERA")
elif (estacion == 4):
    print("OTOÑO")
else:
    print("Opción INCORRECTA")

print("Fin de programa")
